export const stockCardSelect = {
    id: true,
    number: true,
    client_id: true,
    physical_location_id: true,
    physical_location_name: true,
    physical_location_location: true,
    item_id: true,
    item_number: true,
    item_document_url: true,
    item_name: true,
    item_description: true,
    item_price: true,
    unit_id: true,
    unit_code: true,
    unit_name: true,
    type: true,
    reference_no: true,
    quantity: true,
    total_price: true,
    note: true,
    status: true,
    created_by: true,
    created_at: true,
    posted_by: true,
    posted_at: true,
};

export function stockCardSerializer(card) {
    if (!card) return null;
    return {
        id: card.id.toString(),
        number: card.number,
        clientId: card.client_id.toString(),
        physicalLocationId: card.physical_location_id.toString(),
        physicalLocationName: card.physical_location_name,
        physicalLocationLocation: card.physical_location_location,
        itemId: card.item_id.toString(),
        itemNumber: card.item_number,
        itemDocumentUrl: card.item_document_url,
        itemName: card.item_name,
        itemDescription: card.item_description,
        itemPrice: card.item_price?.toString() ?? null,
        unitId: card.unit_id.toString(),
        unitCode: card.unit_code,
        unitName: card.unit_name,
        type: card.type,
        referenceNo: card.reference_no,
        quantity: card.quantity,
        totalPrice: card.total_price?.toString() ?? null,
        note: card.note,
        status: card.status,
        createdBy: card.created_by,
        createdAt: card.created_at,
        postedBy: card.posted_by,
        postedAt: card.posted_at,
    };
}
